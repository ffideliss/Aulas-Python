"""
conversão de tipos, coerção
type convertion, typecasting, coercion
é o ato de convertar um tipo em outro
tipos imutavéis e primitivo:
str, int, float e bool.
"""
print(1 + 1)
print('a' + 'b')
print(int('1') + 1)
print(type(float('1') + 1))
print(int('1'), type(int('1')))
print(bool(' '))
print(str(11) + 'b')